const Actuator = {
  move(direction: string) {
    const tile: HTMLElement = document.querySelector(".grid .tile");

    switch (direction) {
      case c.key.up:
        tile.dataset.row = c.number.one;
        break;

      case c.key.down:
        tile.dataset.row = c.number.four;
        break;

      case c.key.left:
        tile.dataset.column = c.number.one;
        break;

      case c.key.right:
        tile.dataset.column = c.number.four;
        break;

      default:
        break;
    }
  },
};

